/** Pilot teams — the only teams targeted by live re-collection. */
export const PILOT_TEAM_IDS = Object.freeze(
  TEAMS.filter((t) => t.isPilot).map((t) => t.id),
)

export const DEFAULT_TEAM_ID = 'florida-gators'

const BY_ID = new Map(TEAMS.map((t) => [t.id, t]))

export function getTeamById(id: string): Team | undefined {
  return BY_ID.get(id)
}

export function requireTeam(id: string): Team {
  const t = BY_ID.get(id)
  if (!t) throw new Error(`Unknown team id: ${id}`)
  return t
}

export function resolveTeam(value: string): Team | undefined {
  const n = value.trim().toLowerCase()
  return TEAMS.find((t) => t.id === n || t.label.toLowerCase() === n)
}

/** Logo URL resolved via Vite asset handling (src/assets/logos/<id>.png). */
export function teamLogoUrl(id: TeamId): string {
  return new URL(`../assets/logos/${id}.png`, import.meta.url).href
}
